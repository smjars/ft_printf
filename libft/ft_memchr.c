#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	unsigned char	*p;

	p = (unsigned char *)s;
	if (!n)
		return (0);
	while (*p != (unsigned char)c && --n)
		p++;
	if (!n)
		return (0);
	return ((void *)p);
}
